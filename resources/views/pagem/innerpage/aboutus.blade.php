<h3 class="page_heading">आमच्या विषयी</h3>
<h3 style="text-align: center;"><span id="SitePH_CmsContent_lblHeading">झोपडपट्टी पुनर्वसन प्राधिकरण आपले स्वागत करित आहे</span></h3>
<p style="text-align: justify;">महाराष्ट्र शासनाने झोपडपट्टीने व्यापलेली जमीन हा मूलभूत उत्पन्नाचा स्त्रोत धरुन त्यावर चटईक्षेत्र निर्देशांक देऊन, की जेणेकरुन खुल्या बाजारातील सदनिकांच्या विक्रीच्या रकमाद्वारे झोपडपट्टीवासीयांना पक्की घरे मोफत मिळतील, या योजनेसाठी कायद्यामध्ये आवश्यक बदल करुन झोपडपट्टी पुनर्वसन प्राधिकरणची स्थापना केली. हे प्राधिकरण संपूर्ण बृहन्मुंबईतील झोपडपट्टीवासीयांच्या पुनर्वसनासाठी "नियोजन प्राधिकरण" म्हणून काम करील.</p>
<p style="text-align: justify;"> </p>
<p style="text-align: justify;">झोपडपट्टी पुनर्वसन प्राधिकरणाचा प्रयत्न आहे की, झोपडपट्टी पुनर्वसन योजनेची अंमलबजावणी एक खिडकी योजने अंतर्गत राबवावी. म्हणजे प्रकल्पासाठी लागणाऱ्या सर्व प्रकारच्या मान्यता जसे की, सहकारी गृहनिर्माण संस्थेची स्थापना, झोपडपट्टीधारकांची पात्रता प्रमाणित करणे, झोपडपट्टी पुनर्वसन योजनेस असहकार करणाऱ्या विरुद्ध दंडात्मक कारवाई करणे. झोपडपट्टीचे सर्वेक्षण करुन योजना करण्यासाठी अनुमती देणे व गलिच्छवस्तीच्या झोपडपट्टी जमिनीवरील अनुदान मंजूर करणे, पुनर्वसन भूखंडाचे भाडेपट्टा आणि खुले विक्री भूखंड आणि मालमत्ता कार्डस् (पीआर कार्ड) अद्यावत करणे.</p>
<p style="text-align: justify;"> </p>
<p style="text-align: justify;">झोपडपट्टी पुनर्वसन प्राधिकरणाच्या जबाबदाऱ्या:</p>
<ul>
<li style="text-align: justify;">बृहन्मुंबईमधील झोपडपट्टी क्षेत्रासंबंधीच्या विद्यमान स्थितीचे सर्वेक्षण करणे व आढावा घेणे.</li>
<li style="text-align: justify;">झोपडपट्टी क्षेत्रांच्या पुनर्वसनाकरिता योजना तयार करणे.</li>
<li style="text-align: justify;">झोपडपट्टी पुनर्वसन योजनेची अंमलबजावणी करवून घेणे.</li>
<li style="text-align: justify;">झोपडपट्टयांच्या पुनर्वसनाची उद्दिष्टे साध्य करण्यासाठी आवश्यक असतील अशा इतर सर्व कृती व गोष्टी करणे.</li>
</ul>
<p style="text-align: justify;"> </p>
<p style="text-align: justify;"><strong>झोपडपट्टी पुनर्वसन प्राधिकरण - एक नियोजन प्राधिकरण</strong></p>
<p style="text-align: justify;">झोपडपट्टी पुनर्वसन प्राधिकरणास दिनांक 03 जानेवारी 97 पासून " निगम-निकाय दर्जा" प्रदान करण्यात आला आहे. प्राधिकरण ही एक स्वायत्त संस्था आहे. महाराष्ट्र प्रादेशिक व नगर रचना अधिनियम, 1966 मध्ये दुरुस्ती करुन झोपडपट्टी पुनर्वसन प्राधिकरणास त्याच्या कार्यक्षेत्रांतर्गत स्थानिक स्वराज्य संस्था म्हणून कार्य करण्यासाठी नियोजन प्राधिकरण म्हणून घोषित करण्यात आले आहे. राज्य शासनाचे नगर विकास विभाग क्रमांक टी. पी. व्ही. 4396/492/सीआर-105/96/यु.डी. 11 दिनांक 13-9-96 हया अधिसूचनेद्वारे झोपडपट्टी पुनर्वसन प्राधिकरणाच्या मुख्य कार्यकारी अधिकारी यांना महाराष्ट्र प्रादेशिक व नगर रचना अधिनियम, 1966 चे विविध कलमा अंतर्गत वापरता येणारे नियोजन प्राधिकरणाचे सर्व अधिकार प्रदान करण्यात आले आहेत. महाराष्ट्र प्रदेश आणि रचना अधिनियम, 1966 मध्ये दुरुस्ती करुन, झोपडपट्टी पुनर्वसन प्राधिकरणास बृहन्मुंबई विकास आराखडयामध्ये सुधारण करण्यासाठी प्रस्ताव तयार करण्याचे व सादर करण्याचे अधिकार प्रदान केलेले आहेत.</p>
