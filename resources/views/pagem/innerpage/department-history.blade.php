<h3 class="page_heading">विभागीय माहिती</h3>
<p style="text-align: justify;">मुंबई ही महाराष्ट्र राज्याची राजधानी असून तीला नेहमी भारताची आर्थिक राजधानी म्हणून गणली जाते. या शहराच्या रहिवाश्यांना विविध मनोरंजनाच्या सुविधा, समाजिक सुरक्षा, शहरी जीवनाचे वलय, कामाचा चांगला मोबदला, तसेच घरातील कुटुंब प्रमुखाबरोबर इतरही व्यक्तींना पात्रतेनुसार पुरेसे काम मिळण्याची हमी यासारख्या अनेक सुविधा मिळतात. शहराच्या या वैशिष्टयामुळे साहजिकच गेल्या अनेक वर्षात अन्य भागातून मोठया प्रमाणात नागरिकांचे लोंढे या महानगरीकडे आकर्षिले गेले व त्या लोकांनी या महानगरीत वास्तव्य केले. शहरीकरणाच्या प्रचंड रेटयामुळे नियोजनकार, स्थानिक स्वराज्य संस्था, गृहनिर्माण संस्था आणि स्थावर जंगम मालमत्तेचे विकासक या सर्वांना शहरातील सामान्य माणसाला परवडेल अशी रहावयासाठी घरे देणे शक्य झाले नाही. आजच्या मितीला, 50 टक्के पेक्षा जास्त रहिवाशी 2393 पेक्षा गलिच्छ समुहामध्ये विखुरलेले आहेत. ते अतिधोकादायक, आरोग्याला अपायकारक परिस्थितीत दु:खद व असुरक्षित झोपडीत जीवन जगत आहेत. सदर झोपडपट्टया खाजगी जागेत तसेच राज्य सरकारच्या, महानगरपालिकेच्या व केंद्रशासनाच्या आणि गृहनिर्माण मंडळाच्या भूमीवर निर्माण करण्यात आले आहे.</p>
<p style="text-align: justify;">शासनाच्या 1970 पूर्वीच्या धोरणाप्रमाणे या झोपडपट्टया अनधिकृत असल्याने त्या पाडण्यात येवून हटविण्याची कारवाई होत असे. झोपडपट्टी तोडण्याचे प्रयत्न अयशस्वी ठरले, त्याहीपुढे जाऊन, या शहराच्या समाजिक जडणघडणीमध्ये महत्वाचा भाग असणाऱ्या नागरिकांना बेघर करण्याचे, असे संबोधून या प्रयत्नांना अमानवी समजले जाऊ लागले.</p>
<p style="text-align: justify;"> यानंतरच्या दुसऱ्या टप्प्यात या झोपडयांना न तोडता त्यांना मुलभूत नागरी सुविधा व परिसर पर्यावरण सुधारण्याच्या दृष्टीने ठोस कृती करण्याचे ठरले. त्या अनुषंगाने महाराष्ट्र शासनाने महाराष्ट्र झोपडपट्टी क्षेत्र (सुधारणा व निर्मुलन व पुनर्विकास) अधिनियम, 1971 मंजूर केला. त्यामध्ये सुधारकामे स्पष्ट करण्यात आली. सार्वजनिक प्रयोजनासाठी झोपडया हटवायच्या असतील तर त्यांचे इतरत्र पुनर्वसन करण्याचे तत्व स्वीकारण्यात आले.</p>
<p style="text-align: justify;">या पुढच्या टप्प्यामध्ये इ.स. 1980 च्या सुमारास शासनाच्या ध्येय-धोरणामध्ये मुलभूत फरक झाला. या सुमारास जागतिक बँकच्या सहाय्याने झोपडपट्टी श्रेणी वाढ कार्यक्रमाची अंमलबजावणी सुरु झाली.</p>
<p style="text-align: justify;">सध्याच्या परिस्थितीमध्ये महाराष्ट्र शासनाने झोपडपट्टीने व्यापलेली जमीन हा मूलभूत उत्पन्नाचा स्त्रोत धरुन त्यावर चटईक्षेत्र निर्देशांक देऊन, की जेणेकरुन खुल्या बाजारातील सदनिकांच्या विक्रीच्या रकमाद्वारे झोपडपट्टीवासीयांना पक्की घरे मोफत मिळतील.</p>
<p style="text-align: justify;">डिसेंबर 1995 च्या विधानसभा अधिवेशनामध्ये महाराष्ट्र शासनाने अफझूलपूरकर समितीच्या शिफारशी मान्य करुन महाराष्ट्र झोपडपट्टी क्षेत्र (सुधारणा, निर्मूलन व पुनर्विकास) अधिनियम, 1971 मध्ये दुरुस्ती केली व अध्यक्ष, मुख्य कार्यकारी अधिकारी व इतर सदस्यांचा समावेश असलेले झोपडपट्टी पुनर्वसन प्राधिकरण स्थापन करण्याची तरतूद करण्यात आली. दिनांक 16/12/1995 च्या शासकीय अधिसूचने अन्वये झोपडपट्टी पुनर्वसन प्राधिकरणाची स्थापना झाली व ते दिनांक 25/12/1995 पासून कार्यान्वित झाले.</p>
<p style="text-align: justify;"> मा. मुख्यमंत्री, महाराष्ट्र राज्य हे सदर प्राधिकरणाचे अध्यक्ष असून भारतीय प्रशासन सेवेतील अतिकालीत वेतनश्रेणीतील अधिकारी पूर्णवेळ मुख्य कार्यकारी अधिकारी आहेत. इतर सदस्यांमध्ये मंत्री, राज्य विधीमंडळातील निर्वाचित सदस्य, शासनाच्या विविध खात्यांचे सचिव, बांधकाम, नियोजन, सामाजिकसेवा, वास्तूशास्त्र इ. क्षेत्रातील काही तज्ञांचा अशासकीय सदस्यांमध्ये समावेश आहे.</p>
