<h3 class="page_heading">सक्षम सदस्य</h3>
<p style="text-align: justify;">महाराष्ट्र शासनाने, महाराष्ट्र झोपडपट्टी क्षेत्र (सुधारणा, निर्मूलन व पुनर्विकास) अधिनियम, 1971 मध्ये दुरुस्ती केली व अध्यक्ष, मुख्य कार्यकारी अधिकारी व इतर सदस्यांचा समावेश असलेले झोपडपट्टी पुनर्वसन प्राधिकरण स्थापन करण्याची तरतूद करण्यात आली. दिनांक 16/12/1995 च्या शासकीय अधिसूचने अन्वये झोपडपट्टी पुनर्वसन प्राधिकरणाची स्थापना झाली व ते दिनांक 25/12/1995 पासून कार्यान्वित झाले.</p>
<p style="text-align: justify;"> </p>
<p style="text-align: justify;">मा. मुख्यमंत्री, महाराष्ट्र राज्य हे सदर प्राधिकरणाचे पदसिध्द अध्यक्ष असून भारतीय प्रशासन सेवेतील अतिकालीत वेतनश्रेणीतील अधिकारी पूर्णवेळ मुख्य कार्यकारी अधिकारी आहेत. इतर सदस्यांमध्ये मंत्री, राज्य विधीमंडळातील निर्वाचित सदस्य, शासनाच्या विविध खात्यांचे सचिव, बांधकाम, नियोजन, सामाजिकसेवा, वास्तूशास्त्र इ. क्षेत्रातील काही तज्ञांचा अशासकीय सदस्यांमध्ये समावेश आहे. झोपडपट्टी पुनर्वसन प्राधिकरणाचे सध्याचे सदस्य</p>
